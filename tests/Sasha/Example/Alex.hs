alexToken :: BS.ByteString -> Maybe (Tk, BS.ByteString, BS.ByteString)
alexToken bs = case alexScan bs 0 of
    AlexEOF          -> Nothing
    AlexError _      -> Nothing
    AlexSkip bs' _   -> alexToken bs'
    AlexToken _ i tk -> case BS.splitAt i bs of
      (pfx, sfx) -> Just (tk, pfx, sfx)

